RegisterCommand("findhouse", function(player, args)
	if args[1] then
		local houseNr = tonumber(args[1])
		if Houses[houseNr] then
			vRPclient.setGPS(player, {Houses[houseNr].Entry.x, Houses[houseNr].Entry.y})
		else
			vRPclient.sendError(player, {"Nu a fost gasit casa cu numarul introdus !"})
		end
	else
		vRPclient.sendSyntax(player, {"/findhouse <numar>"})
	end
end)